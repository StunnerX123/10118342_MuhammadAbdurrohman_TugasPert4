    private void btnsamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsamaActionPerformed
        // TODO add your handling code here:
        switch(pilih){
            case 1:
                bil2 = Double.parseDouble(String.valueOf(txthasil.getText()));
                hasil = bil1+bil2;
                bil = Double.toString(hasil);
                btntambah.setEnabled(true);
                btnkurang.setEnabled(true);
                btnbagi.setEnabled(true);
                btnkali.setEnabled(true);
                break;
            case 2:
                bil2 = Double.parseDouble(String.valueOf(txthasil.getText()));
                hasil = bil1-bil2;
                bil = Double.toString(hasil);
                btntambah.setEnabled(true);
                btnkurang.setEnabled(true);
                btnbagi.setEnabled(true);
                btnkali.setEnabled(true);
                break;
            case 3:
                bil2 = Double.parseDouble(String.valueOf(txthasil.getText()));
                hasil = bil1/bil2;
                bil = Double.toString(hasil);
                btntambah.setEnabled(true);
                btnkurang.setEnabled(true);
                btnbagi.setEnabled(true);
                btnkali.setEnabled(true);
                break;
            case 4:
                bil2 = Double.parseDouble(String.valueOf(txthasil.getText()));
                hasil = bil1*bil2;
                bil = Double.toString(hasil);
                btntambah.setEnabled(true);
                btnkurang.setEnabled(true);
                btnbagi.setEnabled(true);
                btnkali.setEnabled(true);
                break;
        }
        txthasil.setText(bil);
    }//GEN-LAST:event_btnsamaActionPerformed

